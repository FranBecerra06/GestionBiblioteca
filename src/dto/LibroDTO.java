package dto;

public class LibroDTO {

	private int idLibro;
	private String titulo;
	private int anio;
	public int getIdLibro() {
		return idLibro;
	}
	public String getTitulo() {
		return titulo;
	}
	public int getAnio() {
		return anio;
	}
	
	public String toString() {
		return "LibroDTO [idLibro=" + idLibro + ", titulo=" + titulo + ", anio=" + anio + "]";
	}
	
	
	
	
	
}
